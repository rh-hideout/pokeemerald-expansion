def add_kanto_region(path):
    lines = []
    with open(path, 'r') as file:
        lines = file.readlines()
    
    newlines = []
    with open(path, 'w') as file:
        for line in lines:
            if "region_map_section" in line:
                newlines.append('  "region": "REGION_KANTO",\n')
            newlines.append(line)
        file.writelines(newlines)

def main():
    paths = glob.glob('./data/maps/**/map.json')

    for path in paths:
        m = re.search('data/maps/(\w+)/map.json', path)
        if m:
            map_name = m.group(1)
            if map_name in kanto_maps:
                add_kanto_region(path)

if __name__ == '__main__':
    main()